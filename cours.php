  	<script>
    	
        let selectedAnswer = null;
        let lessonsVisible = true;
        let currentQuestion = 1;
        let totalQuestions = 5;
        let score = 0;
        let timeLeft = 300; 
        let timer = null;
        let answers = {}; 


        const quizData = [
            {
                question: "Question 01 : Quelle est la capitale de la France ?",
                options: ["Paris", "Lyon", "Marseille", "Toulouse"],
                correct: 0
            },
            {
                question: "Question 02 : Combien font 2 + 2 ?",
                options: ["3", "4", "5", "6"],
                correct: 1
            },
            {
                question: "Question 03 : Quel est le plus grand océan ?",
                options: ["Atlantique", "Indien", "Arctique", "Pacifique"],
                correct: 3
            },
            {
                question: "Question 04 : En quelle année a eu lieu la Révolution française ?",
                options: ["1789", "1792", "1799", "1804"],
                correct: 0
            },
            {
                question: "Question 05 : Quelle est la formule chimique de l'eau ?",
                options: ["CO2", "H2O", "O2", "NaCl"],
                correct: 1
            }
        ];


        function initQuiz() {
            updateQuestionDisplay();
            startTimer();
            updateProgressBar();
        }

        function startTimer() {
            timer = setInterval(() => {
                timeLeft--;
                updateTimerDisplay();
                if (timeLeft <= 0) {
                    clearInterval(timer);
                    alert("Temps écoulé ! Quiz terminé.");
                    showResults();
                }
            }, 1000);
        }

        function updateTimerDisplay() {
            const minutes = Math.floor(timeLeft / 60);
            const seconds = timeLeft % 60;
            const timerText = `${minutes}:${seconds.toString().padStart(2, '0')}`;

            let timerElement = document.getElementById('timer-display');
            if (!timerElement) {
                timerElement = document.createElement('div');
                timerElement.id = 'timer-display';
                timerElement.style.position = 'absolute';
                timerElement.style.top = '200px';
                timerElement.style.right = '50px';
                timerElement.style.background = '#4c445e';
                timerElement.style.color = 'white';
                timerElement.style.padding = '10px 20px';
                timerElement.style.borderRadius = '25px';
                timerElement.style.fontFamily = 'Inter';
                timerElement.style.fontSize = '18px';
                timerElement.style.fontWeight = '600';
                document.querySelector('.cours').appendChild(timerElement);
            }
            timerElement.textContent = `⏱️ ${timerText}`;
        }

        function updateQuestionDisplay() {
            const questionData = quizData[currentQuestion - 1];
            document.querySelector('.question-01').textContent = questionData.question;

            const reponseContainer = document.querySelector('.rponse');
            reponseContainer.innerHTML = "";

            questionData.options.forEach((option, index) => {
                const letter = ['A', 'B', 'C', 'D'][index];
                const div = document.createElement("div");
                div.className = "proposition-a";
                div.innerHTML = `
                    <div class="a">${letter}</div>
                    <span>${option}</span>
                `;

                div.onclick = () => selectAnswer(div, letter);

                reponseContainer.appendChild(div);

                if (answers[currentQuestion] === index) {
                    div.classList.add("selected");
                    selectedAnswer = letter;
                }
            });

            selectedAnswer = null;
        }

        function selectAnswer(element, answer) {
            document.querySelectorAll('.proposition-a').forEach(option => {
                option.classList.remove('selected');
            });
            element.classList.add('selected');
            selectedAnswer = answer;


            const answerIndex = ['A', 'B', 'C', 'D'].indexOf(answer);
            answers[currentQuestion] = answerIndex;
        }

        function updateProgressBar() {
            const progressBar = document.querySelector('.jauge-avancement');
            const progressPercentage = (currentQuestion / totalQuestions) * 100;
            const progressWidth = (progressPercentage / 100) * 1030; // largeur max
            progressBar.style.width = progressWidth + 'px';
        }

        function submitAnswer() {
            if (selectedAnswer) {
                if (currentQuestion < totalQuestions) {
                    currentQuestion++;
                    updateQuestionDisplay();
                    updateProgressBar();
                } else {
                    clearInterval(timer);
                    calculateScore();
                    showResults();
                }
            } else {
                const rponseElement = document.querySelector('.rponse');
                rponseElement.style.animation = 'shake 0.5s ease-in-out';
                setTimeout(() => {
                    rponseElement.style.animation = '';
                }, 500);
                alert('Veuillez sélectionner une réponse avant de valider.');
            }
        }

        function calculateScore() {
            score = 0;
            for (let i = 1; i <= totalQuestions; i++) {
                if (answers[i] === quizData[i - 1].correct) {
                    score++;
                }
            }
        }

        function showResults() {
            const percentage = Math.round((score / totalQuestions) * 100);
            let message = `Quiz terminé !\n\nScore: ${score}/${totalQuestions} (${percentage}%)\n\n`;

            if (percentage >= 80) {
                message += "Excellent travail ! 🎉";
            } else if (percentage >= 60) {
                message += "Bon travail ! 👍";
            } else if (percentage >= 40) {
                message += "Peut mieux faire. 📚";
            } else {
                message += "Il faut réviser ! 📖";
            }
            alert(message);
        }

        document.addEventListener('DOMContentLoaded', initQuiz);

            </script></body>
